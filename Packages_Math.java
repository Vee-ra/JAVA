package com.OOP;

/*
 * packages to be combined hierarchically
 * should be starts from "com" in prefix
 */

import java.util.*; //imports all java,util packages's class

//import static java.lang.Math.sqrt; //static import - from SE 5.0

import java.lang.Math.*;

public class Packages_Math {

	public static void main(String[] args) {
		
		System.out.println(Math.sqrt(3));
		
		System.out.println(Math.abs(3.0));
		System.out.println(Math.max(3.0,2.1));
		System.out.println(Math.min(3.0,2.1));
		System.out.println(Math.PI);
		System.out.println(Math.floor(3.95));


	}

}
